plt.scatter(df.area, df.price, color="red", marker="+")

reg.predict([[5000], [3300]])

df_areas = pd.read_csv("/content/areas.csv")
df_areas.head(2)

p = reg.predict(df_areas[["area"]])

df_areas["price_p"] = p

df_areas.head()

df_areas.to_csv("prediction.csv", index=False)

