import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn import linear_model

df = pd.read_csv('/content/homeprices1.csv')
df

reg = linear_model.LinearRegression()
reg.fit(df[["area"]], df.price)

reg.predict([[3300]])

reg.coef_

reg.intercept_

# y = mx + b
# price = m*area + b
